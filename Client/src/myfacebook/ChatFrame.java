    private void sendMessage() {
        if (editTextField.getText().length() > 0) {
            try {
                String response = commModule.getServantStub().sendMessage(mainFrame.getUserID(), friendID, editTextField.getText());
                if (!response.equalsIgnoreCase("success")) {
                    JOptionPane.showMessageDialog(this, response, "Error", JOptionPane.OK_OPTION);
                    return;
                }
            } catch (RemoteException ex) {
                Logger.getLogger(ChatFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            appendMessageToFullChatArea("Me", editTextField.getText());
            editTextField.setText("");
        }
    }
