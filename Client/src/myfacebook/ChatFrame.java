    public void appendMessageToFullChatArea(String contactName, String text) {
        fullChatArea.setForeground(Color.red);
        fullChatArea.append(contactName + ": ");
        fullChatArea.setForeground(Color.black);
        fullChatArea.append(text + "\n");
        fullChatArea.setCaretPosition(fullChatArea.getText().length());
        this.toFront();
    }

